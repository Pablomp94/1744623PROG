public class Matriculas {
    
    private Integer AlumnoId;
    private Integer Profesor;
    private Integer nombreMateria;
    private Integer notaMateria;
    
    
    public Integer getAlumnoId() {
        return AlumnoId;
    }
    public void setAlumnoId(Integer alumnoId) {
        AlumnoId = alumnoId;
    }
    public Integer getProfesor() {
        return Profesor;
    }
    public void setProfesor(Integer profesor) {
        Profesor = profesor;
    }
    public Integer getNombreMateria() {
        return nombreMateria;
    }
    public void setNombreMateria(Integer nombreMateria) {
        this.nombreMateria = nombreMateria;
    }
    public Integer getNotaMateria() {
        return notaMateria;
    }
    public void setNotaMateria(Integer notaMateria) {
        this.notaMateria = notaMateria;
    }


}
